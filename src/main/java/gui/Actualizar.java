    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String []datos = new String[8];
        datos[0] = jTextField1.getText();
        datos[1] = jTextField2.getText();
        datos[2] = jTextField3.getText();
        datos[3] = jTextField4.getText();
        datos[4] = jTextField5.getText();
        datos[5] = jTextField6.getText();
        datos[6] = jTextField7.getText();
        datos[7] = jTextField8.getText();

        for(int k=0; k<jTable1.getColumnCount(); k++){
            jTable1.setValueAt(datos[k], filas, k);
        }
        
        try {
            String archivo = "Empleados.txt";
            BufferedWriter bw = new BufferedWriter(new FileWriter(archivo));
            for(int i=0;i<jTable1.getRowCount(); i++){
                for(int j=0;j<jTable1.getColumnCount(); j++){
                    bw.write((String) (jTable1.getValueAt(i, j)));
                    if(j<jTable1.getColumnCount()-1){
                        bw.write("|");
                    }
                }
                bw.newLine();
            }
            bw.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
        JOptionPane.showMessageDialog(null, "Empleado actualizado correctamente!");
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField8.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

