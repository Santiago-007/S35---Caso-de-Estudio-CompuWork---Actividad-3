    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        mdlTabla = new DefaultTableModel();
        int id = Integer.parseInt(jTextField1.getText());
        String nombre = jTextField2.getText();
        String apellido = jTextField3.getText();
        String fenac = jTextField4.getText();
        String correo = jTextField5.getText();
        String templeado = jTextField6.getText();
        int salario = Integer.parseInt(jTextField7.getText());
        String cargo = jTextField8.getText();
        
        empleado.setId(id);
        empleado.setNombre(nombre);
        empleado.setApellido(apellido);
        empleado.setFecha_nacimiento(fenac);
        empleado.setCorreo(correo);
        empleado.setTipo_empleado(templeado);
        empleado.setSalario(salario);
        empleado.setCargo(cargo);
        
        metodos.agregar(empleado);
        metodos.guardarArchivo(empleado);
        
        JOptionPane.showMessageDialog(null, "¡Empleado agregado exitosamente!");
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField8.setText("");
        
        jTable1.setModel(metodos.listaEmpleados());
        

        
    }//GEN-LAST:event_jButton2ActionPerformed

